public class SwitchCaseEx
	{
public static void main(String args[])
{
	int ch = 5;
	
	switch(ch)
	{
		case 1 :
		System.out.println(" Choice 1 is choosen ");
		break;

		case 2 :
		System.out.println(" Choice 2 is choosen ");
		break;
		
		case 3 :
		System.out.println(" Choice 3 is choosen ");
		break;
		
		case 4 :
		System.out.println(" Choice 4 is choosen ");
		break;

		default:
		System.out.println(" Invalid Choice");
		
	}

}
	}