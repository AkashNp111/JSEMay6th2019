public class SwitchCaseEx2
	{
public static void main(String args[])
{
	String ch = "Idly";
	
	switch(ch)
	{
		case "Idly" :
		System.out.println(" Order Idly Successfull ");
		break;

		case "Voda" :
		System.out.println(" Order Voda Successfull");
		break;
		
		case "Dosa" :
		System.out.println(" Order Dosa Successfull ");
		break;
		
		case "Puri" :
		System.out.println(" Order Puri Successfull ");
		break;

		default:
		System.out.println(" Invalid Choice");
		
	}

}
	}